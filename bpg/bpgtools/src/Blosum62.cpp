int Blosum62::score(char s, char t)
{
    int si = index(s);
    int ti = index(t);

    if (si >= 0 && ti >= 0)
    {
        return matrix[si][ti];
    }

    return 0;
}

int Blosum62::index(char s)
{
    for (int i = 0; i < 24; i++)
    {
        if (aamap[i] == s)
        {
            return i;
        }
    }

    return -1;
}

void Blosum62::printMatrix()
{
    cout << "   ";

    for (int i = 0; i < 24; i++)
    {
        cout << aamap[i] << "  ";
    }

    cout << endl;

    for (int i = 0; i < 24; i++)
    {
        cout << aamap[i];

        for (int j = 0; j < 24; j++)
        {
            int s = score(aamap[i], aamap[j]);

            cout << " ";

            if (s >= 0 && s < 10)
            {
                cout << " ";
            }

            cout << s;
        }

        cout << endl;
    }
}

